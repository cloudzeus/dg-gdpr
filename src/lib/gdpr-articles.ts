/** Normalize article number for lookup (e.g. "5(1)(c)" → try exact, then parent) */
export function findArticle(ref: string): GdprArticle | null {
  const key = ref.trim();
  if (GDPR_ARTICLES[key]) return GDPR_ARTICLES[key];
  // Try parent — e.g. "32(1)(a)" → "32(1)(a)", then "32"
  const parent = key.replace(/\(\w+\)\s*$/, "").trim();
  if (parent !== key && GDPR_ARTICLES[parent]) return GDPR_ARTICLES[parent];
  return null;
}
